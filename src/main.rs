use std::io::Read;

macro_rules! frick {
    ($($code:tt)* ) => {
        {
            let mut mem = [0_u8; 30_000];
            let mut ptr = 0;
            $(
                instr!(mem ptr $code);
            )*
        }
    };
}

macro_rules! instr {
    // optimizations
    // clear loop optimizer
    ($mem:ident $ptr:ident [-]) => {
        $mem[$ptr] = 0;
    };
    // standard tokens / rust required tokens
    ($mem:ident $ptr:ident >) => {
        $ptr += 1;
    };
    ($mem:ident $ptr:ident >>) => {
        $ptr += 2;
    };
    ($mem:ident $ptr:ident <) => {
        $ptr -= 1;
    };
    ($mem:ident $ptr:ident <<) => {
        $ptr -= 2;
    };
    ($mem:ident $ptr:ident +) => {
        $mem[$ptr] = $mem[$ptr].wrapping_add(1);
    };
    ($mem:ident $ptr:ident -) => {
        $mem[$ptr] = $mem[$ptr].wrapping_sub(1);
    };
    ($mem:ident $ptr:ident .) => {
        print!("{}", $mem[$ptr] as char);
    };
    ($mem:ident $ptr:ident ..) => {
        instr!($mem $ptr .);
        instr!($mem $ptr .);
    };
    ($mem:ident $ptr:ident ...) => {
        instr!($mem $ptr .);
        instr!($mem $ptr ..);
    };
    ($mem:ident $ptr:ident ,) => {
        $mem[$ptr] = std::io::stdin()
        .bytes().next().unwrap().unwrap();
    };
    ($mem:ident $ptr:ident ->) => {
        instr!($mem $ptr -);
        instr!($mem $ptr >);
    };
    ($mem:ident $ptr:ident <-) => {
        instr!($mem $ptr <);
        instr!($mem $ptr -);
    };
    ($mem:ident $ptr:ident [ $( $body:tt )* ]) => {
        while $mem[$ptr] > 0 {
            $(
                instr!($mem $ptr $body);
            )*
        }
    };
}

